#include <unistd.h>

void	ft_print_comb2(void)
{
	int	i;
	int	a;

	i = 0;
	while (i <= 98)
	{
		a = i + 1;
		while (a <= 99)
		{
			write(1, &("0123456789"[i / 10]), 1);
			write(1, &("0123456789"[i % 10]), 1);
			write(1, " ", 1);
			write(1, &("0123456789"[a / 10]), 1);
			write(1, &("0123456789"[a % 10]), 1);
			if (!(i == 98 && a == 99))
				write(1, ", ", 2);
			a++;
		}
		i++;
	}
}

